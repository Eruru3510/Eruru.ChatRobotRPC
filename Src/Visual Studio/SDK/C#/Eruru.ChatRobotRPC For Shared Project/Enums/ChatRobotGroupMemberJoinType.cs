namespace Eruru.ChatRobotRPC {

	/// <summary>
	/// 群成员加入类型
	/// </summary>
	public enum ChatRobotGroupMemberJoinType {

		/// <summary>
		/// 某人被批准加入
		/// </summary>
		Approve = 1,
		/// <summary>
		/// 我加入某个群
		/// </summary>
		IJoin,
		/// <summary>
		/// 某人被邀请加入了群
		/// </summary>
		Invite

	}

}